                    entry.target.style.transform = 'translateY(0)';
                    entry.target.style.opacity = '1';
                }
            });
        }, { threshold: 0.1 });

        animateElements.forEach((element, index) => {
            element.style.transform = 'translateY(30px)';
            element.style.opacity = '0';
            element.style.transition = `all 0.6s ease ${index * 0.1}s`;
            observer.observe(element);
        });

        // Floating animation for contact icons
        const contactIcons = document.querySelectorAll('.group .w-12.h-12');
        contactIcons.forEach(icon => {
            icon.addEventListener('mouseenter', function() {
                this.style.transform = 'scale(1.1) rotate(5deg)';
            });
            
            icon.addEventListener('mouseleave', function() {
                this.style.transform = 'scale(1) rotate(0deg)';
            });
        });

        // Parallax effect for background pattern
        window.addEventListener('scroll', () => {
            const scrolled = window.pageYOffset;
            const contactSection = document.getElementById('contact');
            if (contactSection) {
                const bgPattern = contactSection.querySelector('.absolute.inset-0.opacity-10');
                if (bgPattern) {
                    bgPattern.style.transform = `translateY(${scrolled * 0.1}px)`;
                }
            }
        });
    });
